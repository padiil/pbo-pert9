    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        // TODO add your handling code here:

        /*
         * Nama     : Fadhil Gani
         * NPM      : 237006082
         * Kelas    : C
         */
        String tahun = tahunTextField.getText();
        String bulan = (String) bulanComboBox.getSelectedItem();
        String hasil = jLabel3.getText();

        if (tahun.isEmpty() || bulan == null || bulan.equals("")) {
            jOptionPane.showMessageDialog(null, "Harap isi tahun dan pilih bulan sebelum menyimpan!", "Peringatan", jOptionPane.WARNING_MESSAGE);
        } else {
            jOptionPane.showMessageDialog(null, "Hasil perhitungan:\n" + hasil, "Hasil Perhitungan", JOptionPane.INFORMATION_MESSAGE);

            try (BufferedWriter writer = new BufferedWriter(new FileWriter("hasil_perhitungan.txt", true))) { // Menambahkan 'true' agar hasil tidak menimpa
                writer.write("Tahun: " + tahun);
                writer.newLine();
                writer.write("Bulan: " + bulan);
                writer.newLine();
                writer.write("Jumlah Hari: " + hasil);
                writer.newLine();
                writer.newLine();

                jOptionPane.showMessageDialog(null, "Hasil perhitungan telah disimpan!", "Simpan Berhasil", JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException e) {
                jOptionPane.showMessageDialog(null, "Error saat menyimpan hasil!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_simpanButtonActionPerformed

