    private void hitungButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungButtonActionPerformed
        /*
         * Nama     : Fadhil Gani
         * NPM      : 237006082
         * Kelas    : C
         */

        try {
            int tahun = Integer.parseInt(tahunTextField.getText());
            String bulan = (String) bulanComboBox.getSelectedItem();
            int jumlahHari = 0;

            if (tahunTextField.getText().isEmpty()) {
                jOptionPane.showMessageDialog(null, "Error: isian tahun kosong!", "Error", jOptionPane.WARNING_MESSAGE);
                return;
            }

            if (bulan.equals("Februari")) {
                // Cek tahun kabisat
                if ((tahun % 4 == 0 && tahun % 100 != 0) || (tahun % 400 == 0)) {
                    jumlahHari = 29;
                } else {
                    jumlahHari = 28;
                }
            } else if (bulan.equals("April") || bulan.equals("Juni") || bulan.equals("September") || bulan.equals("November")) {
                jumlahHari = 30;
            } else {
                jumlahHari = 31;
            }

            jLabel3.setText("Jumlah hari pada bulan " + bulan + " tahun " + tahun + " adalah " + jumlahHari);
        } catch (NumberFormatException e) {
            jOptionPane.showMessageDialog(null, "Error: Tahun harus berupa angka!", "Error", jOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_hitungButtonActionPerformed

